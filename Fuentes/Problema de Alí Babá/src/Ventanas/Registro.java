    private void btn_registrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarseActionPerformed
        Icon q = new ImageIcon(getClass().getResource("/imagenes/0intentos.png"));
        Icon a = new ImageIcon(getClass().getResource("/imagenes/2intentos.png"));
        Icon w = new ImageIcon(getClass().getResource("/imagenes/feliz.png"));
        int limiteEdad = 2;
        int limiteCont = 8;

        Jugador njugador;
        String direccion = "C:/AliBabá/Jugadores.txt";
        Archivo archivito = new Archivo();
        String nom = txt_nombre.getText();
        String ed = txt_edad.getText();
        String c = txt_contrasena.getText();
        String avatar = imagen;

        njugador = new Jugador(nom, ed, c, 0, 0, 0, 0.0, avatar);

        try {
            if (txt_nombre.getText() == null || txt_nombre.getText().equals("") || txt_contrasena.getText() == null || txt_contrasena.getText().equals("") || txt_confirmarContrasena.getText() == null || txt_confirmarContrasena.getText().equals("") || this.txt_edad.getText() == null) {
                JOptionPane.showMessageDialog(null, "Asegúrate de llenar todos los campos", "ERROR", JOptionPane.CANCEL_OPTION, a);
                return;
            }

            if (!this.txt_edad.getText().matches("[0-9]*")) {
                JOptionPane.showMessageDialog(null, "El campo edad contiene solo números", "ERROR", JOptionPane.CANCEL_OPTION, a);
                return;
            }
            if (txt_edad.getText().length() > limiteEdad) {
                JOptionPane.showMessageDialog(null, "Oh no, ingresa tu edad correctamente", "ERROR", JOptionPane.CANCEL_OPTION, a);
                return;
            }

            if (txt_contrasena.getText().length() < limiteCont) {
                JOptionPane.showMessageDialog(null, "La contraseña debe tener al menos 8 dígitos", "ERROR", JOptionPane.CANCEL_OPTION, a);
                return;
            }
            if (!txt_confirmarContrasena.getText().equals(txt_contrasena.getText())) {
                contador--;
                validar();
                return;
            }
        } catch (HeadlessException ev) {

        }

        if (archivito.archivoExiste(direccion)) {

            try {
                if (buscarJugador(nom) == false) {
                    archivito.guardar(njugador, direccion, IniciarSesion.listaJugadores);
                    JOptionPane.showMessageDialog(null, "Jugador Registrado con éxito", "", JOptionPane.CANCEL_OPTION, w);

                    IniciarSesion p = new IniciarSesion();
                    p.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "El nombre de usuario ya existe, intenta con otro", "INTENTA DE NUEVO", JOptionPane.CANCEL_OPTION, q);

                }

            } catch (IOException | ClassNotFoundException ex) {
                Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);

            }

        } else {
            File file = new File(direccion);
            try {
                archivito.guardar(njugador, direccion, IniciarSesion.listaJugadores);
                JOptionPane.showMessageDialog(null, "Jugador Registrado con éxito", "", JOptionPane.CANCEL_OPTION, w);

            } catch (IOException ex) {
                Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
            }
            IniciarSesion p = new IniciarSesion();
            p.setVisible(true);
            this.dispose();
        }
 

    }//GEN-LAST:event_btn_registrarseActionPerformed

