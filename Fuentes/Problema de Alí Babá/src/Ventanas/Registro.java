    public void validar() {
        switch (contador) {
            case 2:
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden, tienes " + (contador) + " intentos", "Error", JOptionPane.CANCEL_OPTION, d);
                break;
            case 1:
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden, tienes " + (contador) + " intentos", "Error", JOptionPane.CANCEL_OPTION, n);
                break;
            case 0:

                JOptionPane.showMessageDialog(null, "Oh no! Intenta de nuevo", "SIN INTENTOS", JOptionPane.CANCEL_OPTION, e);

                txt_nombre.setText("");
                txt_edad.setText("");
                txt_contrasena.setText("");
                txt_confirmarContrasena.setText("");

                contador = 3;
                Historia.setPrueba1(0);
                Genio.setPrueba2(0);
                break;
            default:
                break;
        }

    }

